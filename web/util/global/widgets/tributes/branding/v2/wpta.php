		    domain = domain.substring(7, domain.length);

		    var e = document.getElementById('CDEV-branding-sales-nav');

		    var children = e.getElementsByTagName('a');

		    for (var i = 0; i < children.length; i++) {

		        var href = children[i].href;

		        if(href.indexOf('/story') > -1 || href.indexOf('/category') > -1){

		        	children[i].href = 'https://' + domain + children[i].href.substring(24, children[i].href.length);

		            children[i].target = "_top";
		        }

		    }

